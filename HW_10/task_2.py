class Rectangle:
    '''–ü—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫'''
    def __init__(self, side1, side2=None):
        if side2 is None:
            self.side1 = self.side2 = side1
        else:
            self.side1 = side1
            self.side2 = side2

    def perimetr(self):
        '''–ü–µ—Ä–∏–º–µ—Ç—Ä'''
        return 2 * (self.side1 + self.side2)

    def area(self):
        '''–ü–ª–æ—â–∞–¥—å'''
        return self.side1 * self.side2


a = Rectangle(5)
b = Rectangle(5, 10)
print(a.perimetr(), a.area())
print(b.perimetr(), b.area())
